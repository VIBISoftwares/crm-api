package com.vibi.crm.model;

public class LeadAppointment {

    public String sno;
    public String lead_sno;
    public String appointment_datetime;
    public String comments;
    public String last_appointment_sno;
    public String is_escalated;
    public String status;
	public String createdby;
	public String createddate;
	public String updatedby;
	public String updateddate;
    
    public String getSno() {
        return sno;
    }
    public void setSno(String sno) {
        this.sno = sno;
    }
    public String getLead_sno() {
        return lead_sno;
    }
    public void setLead_sno(String lead_sno) {
        this.lead_sno = lead_sno;
    }
    public String getAppointment_datetime() {
        return appointment_datetime;
    }
    public void setAppointment_datetime(String appointment_datetime) {
        this.appointment_datetime = appointment_datetime;
    }
    public String getComments() {
        return comments;
    }
    public void setComments(String comments) {
        this.comments = comments;
    }
    public String getLast_appointment_sno() {
        return last_appointment_sno;
    }
    public void setLast_appointment_sno(String last_appointment_sno) {
        this.last_appointment_sno = last_appointment_sno;
    }
    public String getIs_escalated() {
        return is_escalated;
    }
    public void setIs_escalated(String is_escalated) {
        this.is_escalated = is_escalated;
    }
    public String getStatus() {
        return status;
    }
    public void setStatus(String status) {
        this.status = status;
    }
    public String getCreatedby() {
        return createdby;
    }
    public void setCreatedby(String createdby) {
        this.createdby = createdby;
    }
    public String getCreateddate() {
        return createddate;
    }
    public void setCreateddate(String createddate) {
        this.createddate = createddate;
    }
    public String getUpdatedby() {
        return updatedby;
    }
    public void setUpdatedby(String updatedby) {
        this.updatedby = updatedby;
    }
    public String getUpdateddate() {
        return updateddate;
    }
    public void setUpdateddate(String updateddate) {
        this.updateddate = updateddate;
    }


    
}
